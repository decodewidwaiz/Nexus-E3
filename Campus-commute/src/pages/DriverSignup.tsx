import { useState } from "react";
import { useNavigate, Link } from "react-router-dom";
import { z } from "zod";
import MobileLayout from "@/components/MobileLayout";
import FormInput from "@/components/FormInput";
import GradientButton from "@/components/GradientButton";
import { useAuth } from "@/contexts/AuthContext";
import { useToast } from "@/hooks/use-toast";

const nameSchema = z.string()
  .min(2, "Name must be at least 2 characters")
  .regex(/^[a-zA-Z\s]+$/, "Name can only contain letters");

const emailSchema = z.string().email("Invalid email address");

const routes = [
  { value: "1", label: "Route no.1 - Kottur to Campus" },
  { value: "2", label: "Route no.2 - Anna Nagar to Campus" },
  { value: "3", label: "Route no.3 - T Nagar to Campus" },
];

const timings = [
  { value: "06:00 AM", label: "06:00 AM" },
  { value: "07:00 AM", label: "07:00 AM" },
  { value: "08:00 AM", label: "08:00 AM" },
];

const DriverSignup = () => {
  const navigate = useNavigate();
  const { setPendingEmail, setPendingUserData, pendingRole } = useAuth();
  const { toast } = useToast();

  const [fullName, setFullName] = useState("");
  const [routeNo, setRouteNo] = useState("");
  const [timing, setTiming] = useState("");
  const [email, setEmail] = useState("");
  const [termsAccepted, setTermsAccepted] = useState(false);
  const [errors, setErrors] = useState<{ fullName?: string; routeNo?: string; timing?: string; email?: string }>({});

  const validateForm = () => {
    const newErrors: { fullName?: string; routeNo?: string; timing?: string; email?: string } = {};

    try {
      nameSchema.parse(fullName);
    } catch (err) {
      if (err instanceof z.ZodError) {
        newErrors.fullName = err.errors[0]?.message;
      }
    }

    if (!routeNo) {
      newErrors.routeNo = "Please select a route";
    }

    if (!timing) {
      newErrors.timing = "Please select a timing";
    }

    try {
      emailSchema.parse(email);
    } catch (err) {
      if (err instanceof z.ZodError) {
        newErrors.email = err.errors[0]?.message;
      }
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleCreateAccount = () => {
    if (!validateForm()) return;

    if (!termsAccepted) {
      toast({
        title: "Terms & Conditions",
        description: "Please accept the terms and conditions to continue",
        variant: "destructive",
      });
      return;
    }

    setPendingEmail(email);
    setPendingUserData({ fullName, routeNo, timing, role: pendingRole });
    navigate("/set-password");
  };

  return (
    <MobileLayout>
      <div className="flex flex-col min-h-screen px-8 py-12">
        <div className="flex-1">
          <h1 className="text-3xl font-bold text-foreground text-center mt-8 mb-2">
            Driver Sign Up
          </h1>
          <p className="text-muted-foreground text-center mb-10">
            Please provide the details below<br />to create your account
          </p>

          <div className="space-y-4 mb-6">
            <FormInput
              placeholder="Full Name"
              value={fullName}
              onChange={(e) => setFullName(e.target.value.replace(/[^a-zA-Z\s]/g, ""))}
              error={errors.fullName}
            />
            
            <div>
              <select
                value={routeNo}
                onChange={(e) => setRouteNo(e.target.value)}
                className="w-full px-5 py-4 bg-muted rounded-full text-foreground border border-transparent focus:border-primary/30 focus:outline-none transition-colors appearance-none cursor-pointer"
              >
                <option value="" disabled>Route No.</option>
                {routes.map((route) => (
                  <option key={route.value} value={route.value}>{route.label}</option>
                ))}
              </select>
              {errors.routeNo && <p className="text-sm text-destructive mt-1 ml-4">{errors.routeNo}</p>}
            </div>

            <div>
              <select
                value={timing}
                onChange={(e) => setTiming(e.target.value)}
                className="w-full px-5 py-4 bg-muted rounded-full text-foreground border border-transparent focus:border-primary/30 focus:outline-none transition-colors appearance-none cursor-pointer"
              >
                <option value="" disabled>Timing</option>
                {timings.map((t) => (
                  <option key={t.value} value={t.value}>{t.label}</option>
                ))}
              </select>
              {errors.timing && <p className="text-sm text-destructive mt-1 ml-4">{errors.timing}</p>}
            </div>

            <FormInput
              placeholder="Enter your Email"
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              error={errors.email}
            />
          </div>

          <label className="flex items-start gap-3 mb-8 cursor-pointer">
            <div 
              onClick={() => setTermsAccepted(!termsAccepted)}
              className={`w-5 h-5 rounded-full border-2 flex items-center justify-center flex-shrink-0 mt-0.5 transition-colors ${
                termsAccepted ? "bg-primary border-primary" : "border-muted-foreground"
              }`}
            >
              {termsAccepted && (
                <div className="w-2 h-2 bg-primary-foreground rounded-full" />
              )}
            </div>
            <span className="text-sm text-foreground leading-relaxed">
              I agree with the{" "}
              <span className="underline">Terms and Conditions</span>
              {" "}and{" "}
              <span className="underline">Privacy Policy</span>
              {" "}of the app
            </span>
          </label>

          <GradientButton onClick={handleCreateAccount}>
            Create Account
          </GradientButton>
        </div>

        <p className="text-center text-muted-foreground mt-8">
          Already have an account?{" "}
          <Link to="/login" className="text-foreground font-medium hover:underline">
            Log In
          </Link>
        </p>
      </div>
    </MobileLayout>
  );
};

export default DriverSignup;
