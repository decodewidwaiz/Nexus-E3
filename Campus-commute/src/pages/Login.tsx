import { useState } from "react";
import { useNavigate, Link } from "react-router-dom";
import { z } from "zod";
import { Apple, Mail } from "lucide-react";
import MobileLayout from "@/components/MobileLayout";
import FormInput from "@/components/FormInput";
import GradientButton from "@/components/GradientButton";
import { useAuth } from "@/contexts/AuthContext";
import { useToast } from "@/hooks/use-toast";

const emailSchema = z.string().email("Invalid email address").refine((email) => {
  const domain = email.split("@")[1];
  return domain?.endsWith("gmail.com") || domain?.endsWith(".ac.in") || domain?.endsWith(".edu");
}, "Only gmail.com, .ac.in, or .edu emails allowed");

const passwordSchema = z.string().min(8, "Password must be at least 8 characters");

const Login = () => {
  const navigate = useNavigate();
  const { login, pendingRole } = useAuth();
  const { toast } = useToast();
  
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [rememberMe, setRememberMe] = useState(false);
  const [errors, setErrors] = useState<{ email?: string; password?: string }>({});
  const [isLoading, setIsLoading] = useState(false);

  const validateForm = () => {
    const newErrors: { email?: string; password?: string } = {};
    
    try {
      emailSchema.parse(email);
    } catch (err) {
      if (err instanceof z.ZodError) {
        newErrors.email = err.errors[0]?.message;
      }
    }

    try {
      passwordSchema.parse(password);
    } catch (err) {
      if (err instanceof z.ZodError) {
        newErrors.password = err.errors[0]?.message;
      }
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleLogin = async () => {
    if (!validateForm()) return;

    setIsLoading(true);
    try {
      // Check if account exists (simulated)
      const registeredAccounts = JSON.parse(
        localStorage.getItem("campus-commute-accounts") || "[]"
      );
      const accountExists = registeredAccounts.some(
        (acc: any) => acc.email === email && acc.role === pendingRole
      );

      if (!accountExists) {
        toast({
          title: "Account not found",
          description: "Please sign up first.",
          variant: "destructive",
        });
        setIsLoading(false);
        return;
      }

      // Check password
      const account = registeredAccounts.find(
        (acc: any) => acc.email === email && acc.role === pendingRole
      );

      if (account.password !== password) {
        toast({
          title: "Login failed",
          description: "Invalid email or password",
          variant: "destructive",
        });
        setIsLoading(false);
        return;
      }

      const success = await login(email, password, pendingRole);
      if (success) {
        toast({
          title: "Login successful!",
          description: "Welcome back to Campus Commute",
        });
        navigate(pendingRole === "driver" ? "/driver-home" : "/home");
      }
    } catch (err) {
      toast({
        title: "Login failed",
        description: "An error occurred during login",
        variant: "destructive",
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleSocialLogin = (provider: string) => {
    toast({
      title: `${provider} Login`,
      description: "Social login coming soon!",
    });
  };

  return (
    <MobileLayout>
      <div className="flex flex-col min-h-screen px-8 py-12">
        <div className="flex-1">
          <h1 className="text-3xl font-bold text-foreground text-center mt-8 mb-2">
            Log In
          </h1>
          <p className="text-muted-foreground text-center mb-10">
            Please provide the details below to log in
          </p>

          <div className="space-y-4 mb-6">
            <FormInput
              placeholder="Enter your Email"
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              error={errors.email}
            />
            <FormInput
              placeholder="Enter Password"
              type="password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              showPasswordToggle
              error={errors.password}
            />
          </div>

          <div className="flex items-center justify-between mb-8">
            <label className="flex items-center gap-2 cursor-pointer">
              <div 
                onClick={() => setRememberMe(!rememberMe)}
                className={`w-5 h-5 rounded-full border-2 flex items-center justify-center transition-colors ${
                  rememberMe ? "bg-primary border-primary" : "border-muted-foreground"
                }`}
              >
                {rememberMe && (
                  <div className="w-2 h-2 bg-primary-foreground rounded-full" />
                )}
              </div>
              <span className="text-sm text-foreground">Remember Me</span>
            </label>
            <Link 
              to="/forgot-password" 
              className="text-sm text-foreground hover:underline"
            >
              Forgot Password ?
            </Link>
          </div>

          <GradientButton onClick={handleLogin} disabled={isLoading}>
            {isLoading ? "Logging in..." : "Log In"}
          </GradientButton>

          <div className="flex items-center gap-4 my-6">
            <div className="flex-1 h-px bg-border" />
            <span className="text-muted-foreground text-sm">Or continue With</span>
            <div className="flex-1 h-px bg-border" />
          </div>

          <div className="flex gap-4 mb-8">
            <button
              onClick={() => handleSocialLogin("Google")}
              className="flex-1 py-3 px-4 border-2 border-foreground/20 rounded-full text-foreground font-medium hover:bg-muted transition-colors flex items-center justify-center gap-2"
            >
              {/* Google G Logo SVG */}
              <svg width="20" height="20" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
              </svg>
              Google
            </button>
            <button
              onClick={() => handleSocialLogin("Apple")}
              className="flex-1 py-3 px-4 border-2 border-foreground/20 rounded-full text-foreground font-medium hover:bg-muted transition-colors flex items-center justify-center gap-2"
            >
              <Apple size={20} />
              Apple
            </button>
          </div>
        </div>

        <p className="text-center text-muted-foreground">
          Don't have an account?{" "}
          <Link 
            to={pendingRole === "driver" ? "/driver-signup" : "/student-signup"} 
            className="text-foreground font-medium hover:underline"
          >
            Sign Up
          </Link>
        </p>
      </div>
    </MobileLayout>
  );
};

export default Login;
